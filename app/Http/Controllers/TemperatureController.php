    {
        $inputs = $request->all();

        $i=0;
        for ($i=1; $i<= 8; $i++)
        {
            if(isset($inputs['serial'.$i]) && isset($inputs['temperature'.$i]))
                Temperature::create([
                   'sensor_id' => $inputs['serial'.$i],
                    'value' => $inputs['temperature'.$i]
                ]);
        }
    }

